/**
 * Get icon for category/subcategory with improved matching
 */
export function getIcon(name, isMainCategory = false) {
  if (!name) return '📦';
  
  const normalized = name.toLowerCase().trim();
  
  if (isMainCategory) {
    return CATEGORY_ICONS[normalized] || '📦';
  }
  
  // Check for exact match first
  if (SUBCATEGORY_ICONS[normalized]) {
    return SUBCATEGORY_ICONS[normalized];
  }
  
  // Check for partial matches - prioritize longer matches
  const matches = [];
  for (const [key, icon] of Object.entries(SUBCATEGORY_ICONS)) {
    if (normalized.includes(key)) {
      matches.push({ key, icon, length: key.length });
    } else if (key.includes(normalized)) {
      matches.push({ key, icon, length: key.length });
    }
  }
  
  // Return the longest match (most specific)
  if (matches.length > 0) {
    matches.sort((a, b) => b.length - a.length);
    return matches[0].icon;
  }
  
  // Fallback: check for common words in the name
  const words = normalized.split(/[\s&,-]+/);
  for (const word of words) {
    if (word.length > 2 && SUBCATEGORY_ICONS[word]) {
      return SUBCATEGORY_ICONS[word];
    }
  }
  
  return '📦'; // Default icon
}

